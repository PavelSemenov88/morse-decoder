const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    const decodKey3 = {

    };

    for (let key in MORSE_TABLE) {
        const decodKey = key.split("").map(k => k === '.' ? '10' : '11').join('');
        decodKey3[('0000000000' + decodKey).slice(-10)] = MORSE_TABLE[key]

    }

    return expr.split('**********').map(d => d.match(/........../g).map(j => decodKey3[j]).join('')).join(' ');
}

module.exports = {
    decode
}